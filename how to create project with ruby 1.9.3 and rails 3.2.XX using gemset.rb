how to create project with ruby 1.9.3 and rails 3.2.XX using gemset


rvm gemset create rails_3.2_xx

rvm gemset create rails_4.0.xx

rvm gemset list 
  rails_3.2.xx
  rails_4.0.xx

rvm gemset use rails_3.2.xx

rvm use ruby-1.9.3-p484@rails_3.2.xx --create

rvm gemset use rails_3.2.xx

gem install rails --version 3.2.14

rvm use 1.9.3@rails-3.2.3 --default

than set the project with ruby 1.9.3 and rails 3.2.14

